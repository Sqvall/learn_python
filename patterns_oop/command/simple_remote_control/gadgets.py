class Light:
    @staticmethod
    def on():
        print("Light is ON")

    @staticmethod
    def off():
        print("Light is OFF")


class GarageDoor:
    @staticmethod
    def up():
        print("Garage door UP")

    @staticmethod
    def down():
        print("Garage door DOWN")

    @staticmethod
    def stop():
        print("Garage door STOP")

    @staticmethod
    def light_on():
        print("Garage door light is ON")

    @staticmethod
    def light_off():
        print("Garage door light is OFF")
